package com.company;

public class concatenate_function {
    public static void main(String[] args) {
        // it is method of joining two strings togeter
        String a = "hello";
        String b = "Iam Tushar Deshpande";
        System.out.println(a+b);
        System.out.println(a.concat(b));
        System.out.println(a+""+b);
        // the above expressions are used to conatenate (join) two strings
    }
}
